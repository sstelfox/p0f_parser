    (?<source_ip> \g<ipv4>|\g<ipv6>){0}
    (?<source_port> \g<port>){0}

    (?<dest_ip> \g<ipv4>|\g<ipv6>){0}
    (?<dest_port> \g<port>){0}

    \g<source_ip>\/\g<source_port>[ ]->[ ]\g<dest_ip>\/\g<dest_port>[ ]\(\g<rule>\)
  }x

  DATALINE_FORMAT = %r{
    (?<key> \S+){0}
    (?<value> \S+){0}

    \g<key>\s+=\s+\g<value>
  }x
end
